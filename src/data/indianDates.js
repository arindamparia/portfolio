/**
 * Get events for a specific date (MM-DD)
 * Merges fixed events with variable festival dates.
 * @param {string} dateString - Date in MM-DD format (e.g., "08-15")
 * @returns {Array} - Array of events or null if none found
 */
export const getEventsForDate = (dateString) => {
    const fixedEvents = indianEvents[dateString] || [];
    const variableEvents = variableFestivalDates[dateString] || [];

    const allEvents = [...fixedEvents, ...variableEvents];
    return allEvents.length > 0 ? allEvents : null;
};

/**
 * Get a random general fact
 * @returns {string} - A random fact
 */
export const getRandomFact = () => {
    return generalFacts[Math.floor(Math.random() * generalFacts.length)];
};
